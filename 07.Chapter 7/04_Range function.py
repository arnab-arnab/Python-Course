# RANGE FUNCTION

for i in range(8):
    print(i)

for i in range(1,8):   # It prints from 1 to 7
    print(i)

for i in range(5,8):   # It prints from 5 to 7
    print(i)

print("********************")

# STEP SIZE

for i in range(0,8,2): # It skips one value and jumps to the next
    print(i)

for i in range(0,8,3): # It skips two values and jumps to the next
    print(i)
